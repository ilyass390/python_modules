    flaming = enchantment_factory("Flaming")
    frozen = enchantment_factory("Frozen")
    print(flaming("Sword"))
    print(frozen("Shield"))

    # --- memory_vault demo ---
    print("\nTesting memory vault...")
    vault = memory_vault()
